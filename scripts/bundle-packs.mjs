#!/usr/bin/env node
/**
 * Bundle every pack JSON in packs/ into a single TypeScript module that the
 * Worker can import without runtime filesystem access.
 *
 * Output: workers/api/src/packs/all.generated.ts
 *
 * Usage: node scripts/bundle-packs.mjs
 */
import { readdirSync, readFileSync, writeFileSync, mkdirSync, existsSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");
const PACKS_DIR = join(ROOT, "packs");
const OUT_FILE = join(ROOT, "workers/api/src/packs/all.generated.ts");

const types = ["category", "dark-pattern", "regulation", "fee", "intervention"];
const loaded = [];

for (const type of types) {
  const dir = join(PACKS_DIR, type);
  if (!existsSync(dir)) continue;
  for (const file of readdirSync(dir)) {
    if (!file.endsWith(".json")) continue;
    const raw = readFileSync(join(dir, file), "utf8");
    const pack = JSON.parse(raw);
    if (pack.type !== type) {
      throw new Error(`${file}: declared type '${pack.type}' doesn't match directory '${type}'`);
    }
    loaded.push(pack);
  }
}

// Sort for deterministic output
loaded.sort((a, b) => a.slug.localeCompare(b.slug));

const counts = Object.fromEntries(types.map((t) => [t, loaded.filter((p) => p.type === t).length]));

const out = `// AUTO-GENERATED by scripts/bundle-packs.mjs. Do not edit.
// Pack count by type: ${JSON.stringify(counts)}.
// Total packs: ${loaded.length}.

import type { Pack } from "@lens/shared";

export const ALL_PACKS: Pack[] = ${JSON.stringify(loaded, null, 2)} as unknown as Pack[];

export const PACK_COUNTS = ${JSON.stringify(counts)} as const;
`;

mkdirSync(dirname(OUT_FILE), { recursive: true });
writeFileSync(OUT_FILE, out);
console.log(`Wrote ${loaded.length} packs to ${OUT_FILE}`);
console.log(`  ${types.map((t) => `${t}=${counts[t]}`).join(", ")}`);
